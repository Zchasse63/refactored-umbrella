/**
 * E2E account setup — gives BOTH placeholder QA accounts a known password and
 * writes the credentials to .env.e2e (gitignored via the `.env*` rule) for the
 * Playwright auth setup project (e2e/auth.setup.ts).
 *
 *   npm run test:e2e:setup
 *
 * What it does:
 *   1. Reads NEXT_PUBLIC_SUPABASE_URL / NEXT_PUBLIC_SUPABASE_ANON_KEY /
 *      SUPABASE_SERVICE_ROLE_KEY from .env.local (or the process env when the
 *      file is absent, e.g. CI — same convention as scripts/audit-data.mjs).
 *   2. Generates ONE strong random password and sets it on BOTH accounts via
 *      the service-role admin API (auth.admin.updateUserById).
 *   3. Verifies each account can actually sign in with the new password.
 *   4. Writes .env.e2e with E2E_* credentials + the supabase url/anon key.
 *
 * NOTE: this rotates the QA passwords — any previous QA_OWNER_PASSWORD /
 * QA_PARTNER_PASSWORD noted elsewhere (e.g. .env.local) become stale.
 */
import { randomBytes } from "node:crypto";
import { existsSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { createClient } from "@supabase/supabase-js";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");

const ACCOUNTS = [
  { role: "OWNER", email: "owner@theportal.app" },
  { role: "PARTNER", email: "partner@theportal.app" },
];

// ── env: .env.local when present, otherwise the process env (CI) ──
const envFile = path.join(ROOT, ".env.local");
const fileEnv = {};
if (existsSync(envFile)) {
  for (const line of readFileSync(envFile, "utf8").split(/\r?\n/)) {
    const m = line.match(/^([A-Za-z_][A-Za-z0-9_]*)=(.*)$/);
    if (m) fileEnv[m[1]] = m[2].trim();
  }
}
const env = (k) => process.env[k] ?? fileEnv[k];

const url = env("NEXT_PUBLIC_SUPABASE_URL");
const anonKey = env("NEXT_PUBLIC_SUPABASE_ANON_KEY");
const serviceKey = env("SUPABASE_SERVICE_ROLE_KEY");
if (!url || !anonKey || !serviceKey) {
  console.error(
    "Missing NEXT_PUBLIC_SUPABASE_URL / NEXT_PUBLIC_SUPABASE_ANON_KEY / SUPABASE_SERVICE_ROLE_KEY (checked .env.local and the environment).",
  );
  process.exit(1);
}

const admin = createClient(url, serviceKey, {
  auth: { persistSession: false, autoRefreshToken: false },
});

// ── find both accounts (paginate defensively; the instance has a handful of users) ──
const users = [];
for (let pageNo = 1; pageNo <= 10; pageNo += 1) {
  const { data, error } = await admin.auth.admin.listUsers({ page: pageNo, perPage: 100 });
  if (error) {
    console.error("listUsers failed:", error.message);
    process.exit(1);
  }
  users.push(...data.users);
  if (data.users.length < 100) break;
}

// ONE strong random password shared by both accounts (24 chars base64url + fixed
// prefix so every character class is guaranteed regardless of what randomness drew).
const password = `E2e!${randomBytes(18).toString("base64url")}`;

for (const account of ACCOUNTS) {
  const user = users.find((u) => u.email?.toLowerCase() === account.email);
  if (!user) {
    console.error(`Account not found in Supabase auth: ${account.email}`);
    process.exit(1);
  }
  const { error } = await admin.auth.admin.updateUserById(user.id, { password });
  if (error) {
    console.error(`Failed to set password for ${account.email}:`, error.message);
    process.exit(1);
  }
  console.log(`Password set for ${account.email} (${user.id})`);
}

// ── verify: both accounts must actually sign in with the new password ──
for (const account of ACCOUNTS) {
  const client = createClient(url, anonKey, {
    auth: { persistSession: false, autoRefreshToken: false },
  });
  const { data, error } = await client.auth.signInWithPassword({
    email: account.email,
    password,
  });
  if (error || !data.session) {
    console.error(`Sign-in verification FAILED for ${account.email}:`, error?.message);
    process.exit(1);
  }
  await client.auth.signOut({ scope: "local" });
  console.log(`Sign-in verified for ${account.email}`);
}

// ── write .env.e2e (gitignored by the .env* rule) ──
const owner = ACCOUNTS.find((a) => a.role === "OWNER");
const partner = ACCOUNTS.find((a) => a.role === "PARTNER");
const out = `# ── The Portal — E2E test credentials (generated by scripts/e2e-setup-accounts.mjs) ──
# Gitignored (.env* rule). Regenerate any time with: npm run test:e2e:setup
NEXT_PUBLIC_SUPABASE_URL=${url}
NEXT_PUBLIC_SUPABASE_ANON_KEY=${anonKey}
E2E_OWNER_EMAIL=${owner.email}
E2E_OWNER_PASSWORD=${password}
E2E_PARTNER_EMAIL=${partner.email}
E2E_PARTNER_PASSWORD=${password}
`;
writeFileSync(path.join(ROOT, ".env.e2e"), out, { mode: 0o600 });
console.log(`Wrote ${path.join(ROOT, ".env.e2e")}`);
